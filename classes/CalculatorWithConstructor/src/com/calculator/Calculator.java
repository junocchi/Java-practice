package com.calculator;

public class Calculator {
	
	//Both are of int data type.
	public int add(int num1, int num2) {
		return num1 + num2;
	}
	
	public double diff(int num1, int num2) {
		return num1 - num2;
	}
	
	public double mul(int num1, int num2) {
		return num1 * num2;
	}
	
	public double div(int num1, int num2) {
		return num1 / num2;
	}
	
	
	//Both are of double data type.
	public double add(double num1, double num2) {
		return num1 + num2;
	}
	
	public double diff(double num1, double num2) {
		return num1 - num2;
	}
	
	public double mul(double num1, double num2) {
		return num1 * num2;
	}
	
	public double div(double num1, double num2) {
		return num1 / num2;
	}
	
	
	//First parameter is of int data type and second parameter is of double data type.
	public double add(int num1, double num2) {
		return num1 + num2;
	}
	
	public double diff(int num1, double num2) {
		return num1 - num2;
	}
	
	public double mul(int num1, double num2) {
		return num1 * num2;
	}
	
	public double div(int num1, double num2) {
		return num1 / num2;
	}
	
	//First parameter is of double data type and second parameter is of int data type.
	public double add(double num1, int num2) {
		return num1 + num2;
	}
	
	public double diff(double num1, int num2) {
		return num1 - num2;
	}
	
	public double mul(double num1, int num2) {
		return num1 * num2;
	}
	
	public double div(double num1, int num2) {
		return num1 / num2;
	}
	
	
}
